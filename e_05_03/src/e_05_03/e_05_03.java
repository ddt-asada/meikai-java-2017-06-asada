package e_05_03;

public class e_05_03 {

	public static void main(String[] args) {
		boolean btest1 = true;	//bool型にtrueを代入。
		boolean btest2 = false;//bool型にfalseを代入。

		//代入した値を表示して確かめる。
		System.out.println("btest1 = " + btest1);
		System.out.println("btest2 = " + btest2);
	}

}
